    'current_row_count=' || COUNT(*) AS issue_details
FROM warehouse.dim_product
WHERE product_id <> 'UNKNOWN'
  AND is_current
GROUP BY product_id
HAVING COUNT(*) > 1

UNION ALL

SELECT
    'duplicate_campaign_current_rows_after_reprocess' AS check_name,
    'warehouse.dim_campaign' AS checked_entity,
    campaign_id AS natural_key,
    STRING_AGG(campaign_sk::TEXT, ', ' ORDER BY campaign_sk) AS surrogate_key,
    NULL::TEXT AS fact_key,
    'current_row_count=' || COUNT(*) AS issue_details
FROM warehouse.dim_campaign
WHERE campaign_id <> 'UNKNOWN'
  AND is_current
GROUP BY campaign_id
HAVING COUNT(*) > 1;

