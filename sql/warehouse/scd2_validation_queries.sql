-- 3. No overlapping effective date ranges
-- =============================================================================

CREATE OR REPLACE VIEW audit.v_scd2_overlapping_range_violations AS
SELECT
    'overlapping_customer_effective_ranges' AS check_name,
    'warehouse.dim_customer' AS checked_entity,
    a.customer_id AS natural_key,
    a.customer_sk::TEXT || ' overlaps ' || b.customer_sk::TEXT AS surrogate_key,
    NULL::TEXT AS fact_key,
    'a=[' || a.effective_from || ', ' || a.effective_to || '), b=['
        || b.effective_from || ', ' || b.effective_to || ')' AS issue_details
FROM warehouse.dim_customer a
JOIN warehouse.dim_customer b
    ON a.customer_id = b.customer_id
   AND a.customer_sk < b.customer_sk
