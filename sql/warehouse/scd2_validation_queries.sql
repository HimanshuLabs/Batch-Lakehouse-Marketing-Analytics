WITH run_context AS (
    SELECT
        to_char(clock_timestamp(), 'YYYYMMDDHH24MISSMS') AS validation_run_id,
        clock_timestamp() AS checked_at
),
validation_counts AS (
    SELECT
        'SCD2_DIMENSION_INTEGRITY' AS check_category,
        'only_one_current_row_per_natural_key' AS check_name,
        'customer/product/campaign dimensions' AS checked_entity,
        COUNT(*)::BIGINT AS failed_row_count,
        'Each natural key must have exactly one current dimension row.' AS details
    FROM audit.v_scd2_current_row_violations

    UNION ALL

    SELECT
        'SCD2_DIMENSION_INTEGRITY',
        'no_invalid_effective_date_ranges',
        'customer/product/campaign dimensions',
        COUNT(*)::BIGINT,
        'Every SCD2 row must have effective_to greater than effective_from.'
    FROM audit.v_scd2_invalid_effective_range_violations

    UNION ALL

    SELECT
        'SCD2_DIMENSION_INTEGRITY',
        'no_overlapping_effective_date_ranges',
        'customer/product/campaign dimensions',
        COUNT(*)::BIGINT,
        'Historical versions for the same natural key must not overlap.'
    FROM audit.v_scd2_overlapping_range_violations

    UNION ALL

    SELECT
        'SCD2_SURROGATE_KEY_INTEGRITY',
        'no_duplicate_surrogate_keys',
        'customer/product/campaign dimensions',
        COUNT(*)::BIGINT,
        'Surrogate keys must be unique across each SCD2 dimension.'
    FROM audit.v_scd2_duplicate_surrogate_key_violations

    UNION ALL

    SELECT
        'FACT_FOREIGN_KEY_INTEGRITY',
        'no_missing_customer_product_campaign_sk_in_facts',
        'warehouse fact tables',
        COUNT(*)::BIGINT,
        'Facts must not contain NULL or unknown surrogate keys for required SCD2 dimensions.'
    FROM audit.v_scd2_missing_fact_surrogate_key_violations

    UNION ALL

    SELECT
        'POINT_IN_TIME_CORRECTNESS',
        'fact_dates_fall_inside_dimension_effective_ranges',
        'transactional warehouse fact tables joined to SCD2 dimensions',
        COUNT(*)::BIGINT,
        'Transactional fact timestamps must fall inside the effective window of their joined dimension version. Web events and conversions are excluded because behavioral event facts use inferred/current-dimension fallback when no exact SCD2 window exists.'
    FROM audit.v_scd2_fact_date_range_violations

    UNION ALL

    SELECT
        'SCD2_NULL_HANDLING',
        'null_tracked_attributes_do_not_create_duplicate_versions',
        'customer/product/campaign dimensions',
        COUNT(*)::BIGINT,
        'Null-normalized tracked attributes must not create duplicate equivalent SCD2 versions.'
    FROM audit.v_scd2_null_tracked_attribute_violations

    UNION ALL

    SELECT
        'SCD2_REPROCESSING_IDEMPOTENCY',
        'reprocessing_does_not_create_duplicate_current_or_version_rows',
        'customer/product/campaign dimensions',
        COUNT(*)::BIGINT,
        'Reprocessing must not create duplicate current rows or duplicate natural-key/effective-from versions.'
    FROM audit.v_scd2_reprocessing_idempotency_violations
)
INSERT INTO audit.scd2_validation_results (
    validation_run_id,
    check_category,
    check_name,
    checked_entity,
    failed_row_count,
    status,
    severity,
    details,
    checked_at
)
SELECT
    rc.validation_run_id,
    vc.check_category,
    vc.check_name,
    vc.checked_entity,
    vc.failed_row_count,
    CASE
        WHEN vc.failed_row_count = 0 THEN 'PASS'
        ELSE 'FAIL'
    END AS status,
    'ERROR' AS severity,
    vc.details,
    rc.checked_at
FROM validation_counts vc
CROSS JOIN run_context rc;

COMMIT;

