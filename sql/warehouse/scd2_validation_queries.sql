/* ===============================================================================
PostgreSQL Warehouse SCD2 Validation Queries
================================================================================

Purpose:
- Validate SCD Type 2 correctness for warehouse.dim_customer,
  warehouse.dim_product, and warehouse.dim_campaign.
- Prove that facts join to the correct historical dimension versions.
- Catch duplicate current rows, invalid effective windows, overlapping ranges,
  duplicate surrogate keys, missing/unknown surrogate keys in facts, and
  reprocessing/idempotency failures.
- Store validation results in audit.scd2_validation_results.

Expected result:
- Every validation row should return PASS.
- Any FAIL row means warehouse history is not trustworthy yet.

================================================================================ */

BEGIN;

CREATE SCHEMA IF NOT EXISTS audit;

DROP TABLE IF EXISTS audit.scd2_validation_results CASCADE;

CREATE TABLE audit.scd2_validation_results (
    validation_result_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    validation_run_id TEXT NOT NULL,
    check_category TEXT NOT NULL,
    check_name TEXT NOT NULL,
    checked_entity TEXT NOT NULL,
    failed_row_count BIGINT NOT NULL,
    status TEXT NOT NULL CHECK (status IN ('PASS', 'FAIL')),
    severity TEXT NOT NULL DEFAULT 'ERROR',
    details TEXT,
    checked_at TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);

COMMENT ON TABLE audit.scd2_validation_results IS
'SCD2 validation audit table. Stores pass/fail counts for warehouse historical correctness checks.';

-- =============================================================================
-- 1. Only one current row per natural key
-- =============================================================================

CREATE OR REPLACE VIEW audit.v_scd2_current_row_violations AS
SELECT
    'only_one_current_row_per_customer_id' AS check_name,
    'warehouse.dim_customer' AS checked_entity,
    customer_id AS natural_key,
