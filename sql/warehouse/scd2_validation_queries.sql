WITH latest_run AS (
    SELECT validation_run_id
    FROM audit.scd2_validation_results
    ORDER BY checked_at DESC
    LIMIT 1
)
SELECT
    validation_run_id,
    status,
    COUNT(*) AS check_count,
    SUM(failed_row_count) AS total_failed_rows
FROM audit.scd2_validation_results
WHERE validation_run_id = (SELECT validation_run_id FROM latest_run)
GROUP BY validation_run_id, status
ORDER BY status;

